package br.com.api.picos.objetos;

public class Endereco {
	int idEd;
	String idEdUsuario;
	String cepEd;
	String ruaEd;
	String bairroEd;
	String cidadeEd;
	String ufEd;
	String numeroEd;
	String complementoEd;
	
	public int getIdEd() {
		return idEd;
	}
	public void setIdEd(int idEd) {
		this.idEd = idEd;
	}
	public String getIdEdUsuario() {
		return idEdUsuario;
	}
	public void setIdEdUsuario(String idEdUsuario) {
		this.idEdUsuario = idEdUsuario;
	}
	public String getCepEd() {
		return cepEd;
	}
	public void setCepEd(String cepEd) {
		this.cepEd = cepEd;
	}
	public String getRuaEd() {
		return ruaEd;
	}
	public void setRuaEd(String ruaEd) {
		this.ruaEd = ruaEd;
	}
	public String getBairroEd() {
		return bairroEd;
	}
	public void setBairroEd(String bairroEd) {
		this.bairroEd = bairroEd;
	}
	public String getCidadeEd() {
		return cidadeEd;
	}
	public void setCidadeEd(String cidadeEd) {
		this.cidadeEd = cidadeEd;
	}
	public String getUfEd() {
		return ufEd;
	}
	public void setUfEd(String ufEd) {
		this.ufEd = ufEd;
	}
	public String getNumeroEd() {
		return numeroEd;
	}
	public void setNumeroEd(String numeroEd) {
		this.numeroEd = numeroEd;
	}
	public String getComplementoEd() {
		return complementoEd;
	}
	public void setComplementoEd(String complementoEd) {
		this.complementoEd = complementoEd;
	}
}
